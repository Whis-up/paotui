package com.xiaozhao.paotui.intf.entity;



import lombok.Data;

import java.util.Date;
@Data
public class Address {

    private int id;

    private int userId;

    private int collegeLocationId;

    private String address;

    private String name;

    private String phone;

    private int status;


    private Date addTime;


    private Date updateTime;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public int getCollegeLocationId() {
        return collegeLocationId;
    }

    public void setCollegeLocationId(int collegeLocationId) {
        this.collegeLocationId = collegeLocationId;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public Date getAddTime() {
        return addTime;
    }

    public void setAddTime(Date addTime) {
        this.addTime = addTime;
    }

    public Date getUpdateTime() {
        return updateTime;
    }

    public void setUpdateTime(Date updateTime) {
        this.updateTime = updateTime;
    }
}
